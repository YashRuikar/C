// Area of cicle and volume of cylinder

#include<stdio.h>

int main()
{
    int radius =3;
    float pi=3.14;
    printf("The area of this circle is %f\n", pi*radius*radius);
    int height=3;
    printf("Volume of this cylinderis %f\n ", pi*radius*radius*height);
    return 0;
}
// The area of this circle is 28.260001
// Volume of this cylinderis 84.780003